<script>
    const tabButtons = document.querySelectorAll('[data-tab]');
    const tabPanels = document.querySelectorAll('.tab-panel');

    tabButtons.forEach(button => {
        button.addEventListener('click', () => {
            const tab = button.getAttribute('data-tab');
            tabPanels.forEach(panel => {
                panel.classList.toggle('hidden', panel.id !== tab);

            });
            tabButtons.forEach(btn => {
                btn.classList.remove('bg-orange-500');
                btn.classList.add('bg-orange-400');
            });

            button.classList.remove('bg-orange-400');
            button.classList.add('bg-orange-500');
        });
    });

    // Mostrar la primera pestaña por defecto
    document.querySelector('.tab-panel').classList.remove('hidden');
    document.querySelector('[data-tab]').classList.add('bg-orange-500');
</script>